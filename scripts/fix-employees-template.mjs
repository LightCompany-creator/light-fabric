// Корректирует employees-template.xlsx:
//   • Заполняет пропущенные коды цехов на основе должности
//   • Исправляет очевидные несоответствия должность ↔ цех
//   • Заполняет роли руководству (директор / админ / бухгалтер)
//   • Обновляет лист «Цеха» (RAW → ANG, добавляет LST)
//   • Обновляет лист «Как заполнить»
//
// Запуск: node scripts/fix-employees-template.mjs

import * as XLSX from "xlsx";
import path from "node:path";
import fs from "node:fs";

const filePath = path.resolve(process.cwd(), "employees-template.xlsx");
const buf = fs.readFileSync(filePath);
const wb = XLSX.read(buf, { type: "buffer", cellDates: true });

const WORKERS_SHEET = "Работники";
const WORKSHOPS_SHEET = "Цеха";
const NOTES_SHEET = "Как заполнить";

// =============================================================
// 1) Работники
// =============================================================
const ws = wb.Sheets[WORKERS_SHEET];
const rows = XLSX.utils.sheet_to_json(ws, { defval: null, raw: false });

// Карта правок: ключ — табельный номер, значение — что менять.
// На текущий момент исходный файл уже соответствует ожиданиям —
// все начальные правки были применены ранее, а пользователь сама
// дополнила недостающие роли. Этот объект оставлен пустым, чтобы
// повторный запуск скрипта не затирал ручные правки.
// Сам скрипт по-прежнему обновляет листы «Цеха» и «Как заполнить».
const PATCHES = {};

const log = [];
const patched = rows.map((row) => {
  const tab = String(row["Табельный номер"] ?? "").trim();
  const patch = PATCHES[tab];
  if (!patch) return row;
  const updates = {};
  for (const [field, newVal] of Object.entries(patch)) {
    const oldVal = row[field];
    if (oldVal !== newVal) {
      updates[field] = `${oldVal ?? "(пусто)"} → ${newVal}`;
    }
  }
  if (Object.keys(updates).length > 0) {
    log.push({ tab, ФИО: row["ФИО"], изменения: updates });
  }
  return { ...row, ...patch };
});

// Перезаписываем лист
const HEADERS = [
  "Табельный номер",
  "ФИО",
  "Цех",
  "Должность",
  "Роль",
  "Активен",
  "Дата приёма",
];
const sheetData = [HEADERS, ...patched.map((r) => HEADERS.map((h) => r[h] ?? ""))];
const newWs = XLSX.utils.aoa_to_sheet(sheetData);
newWs["!cols"] = [
  { wch: 16 }, { wch: 38 }, { wch: 8 }, { wch: 38 },
  { wch: 14 }, { wch: 10 }, { wch: 14 },
];
wb.Sheets[WORKERS_SHEET] = newWs;

// =============================================================
// 2) Лист «Цеха» — обновляем под актуальные коды
// =============================================================
const WORKSHOPS = [
  ["Код", "Название", "Что внутри"],
  ["ANG",  "Ангар", "Производство гранул ЭВА и хранение сырья"],
  ["LIT",  "Литейка", "Литейный цех (ИЛМ)"],
  ["PACK", "Упаковка", "Упаковка-диспетчер: обрезка облоя, сортировка"],
  ["CUT",  "Крой", "Раскрой текстиля и меха"],
  ["SEW",  "Швейка", "Сборка вкладышей и подкладок"],
  ["GLU",  "Клеевой", "Вклейка вкладышей, склейка подошв"],
  ["ASSY", "Обшив", "Установка фурнитуры и ремешков"],
  ["MARK", "Маркировка", "Логотип, артикул, ОТК"],
  ["SHIP", "Склад", "Хранение и отгрузка готовой продукции"],
  ["ADM",  "Администрация", "Дирекция, бухгалтерия, ИТ — вне производственного потока"],
  ["LST",  "Листы", "Все не-обувные направления: ЭВА-листы, коврики, автонакидки, стропа, тесьма"],
];
const wsWorkshops = XLSX.utils.aoa_to_sheet(WORKSHOPS);
wsWorkshops["!cols"] = [{ wch: 8 }, { wch: 18 }, { wch: 70 }];
wb.Sheets[WORKSHOPS_SHEET] = wsWorkshops;

// =============================================================
// 3) Лист «Как заполнить» — обновляем инструкцию по столбцу «Цех»
// =============================================================
const NOTES = [
  ["Поле", "Обязательно", "Что писать"],
  ["Табельный номер", "ДА", "Уникальный код работника (любой формат: 0042, ТЗ-1, и т.п.)"],
  ["ФИО", "ДА", "Полное имя"],
  ["Цех", "желательно", "Код цеха из листа «Цеха». Обувь: ANG/LIT/PACK/CUT/SEW/GLU/ASSY/MARK/SHIP. Не-обувная линия: LST. Администрация: ADM. Можно пусто, если работник не привязан к цеху."],
  ["Должность", "желательно", "Свободный текст: Литейщик, Швея, Начальник цеха, Технолог, Директор и т.п."],
  ["Роль", "только если человек будет ЛОГИНИТЬСЯ", "foreman (начальник цеха) / technologist (технолог) / director (директор) / accountant (бухгалтер) / admin. Рядовым работникам — пусто."],
  ["Активен", "нет (по умолчанию — да)", "да / нет / 1 / 0"],
  ["Дата приёма", "нет", "Формат дд.мм.гггг или гггг-мм-дд"],
  ["", "", ""],
  ["⚠ Примечания", "", ""],
  ["", "", "• Уникальный ключ — Табельный номер. Если в системе уже есть работник с таким номером — данные обновятся, не задублируются."],
  ["", "", "• Импорт не создаёт логин/пароль. Это шаг 1 — заносим табель. Создание Auth-аккаунтов для тех, у кого есть роль — отдельный шаг."],
  ["", "", "• Заголовки можно переименовывать в snake_case: tab_number, full_name, workshop_code, position, role, is_active, hire_date. Парсер поймёт оба варианта."],
];
const wsNotes = XLSX.utils.aoa_to_sheet(NOTES);
wsNotes["!cols"] = [{ wch: 22 }, { wch: 38 }, { wch: 100 }];
wb.Sheets[NOTES_SHEET] = wsNotes;

// =============================================================
// Запись
// =============================================================
XLSX.writeFile(wb, filePath);

console.log(`✓ Файл сохранён: ${filePath}\n`);
console.log("Изменения:\n");
for (const item of log) {
  console.log(`  ${item.tab}  ${item.ФИО}`);
  for (const [k, v] of Object.entries(item.изменения)) {
    console.log(`     · ${k}: ${v}`);
  }
}
console.log(`\nВсего записей в файле: ${patched.length}`);
console.log("Листы «Цеха» и «Как заполнить» обновлены под актуальную структуру (11 цехов).");
